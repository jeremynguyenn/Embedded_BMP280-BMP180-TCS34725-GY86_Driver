#include <linux/ioctl.h>

#define BMP180_MAGIC 0x2B

#define BMP180_GET_TEMP 0x70
#define BMP180_GET_PRES 0x71
#define BMP180_GET_ALTI 0x72
#define BMP180_GET_OSS  0x73
#define BMP180_SET_OSS  0x74
#define BMP180_COEF     0x75
#define BMP180_DBGMSG   0x76



#define IOCTL_BMP180_GET_TEMP _IOR(BMP180_MAGIC, BMP180_GET_TEMP, char*)
#define IOCTL_BMP180_GET_PRES _IOR(BMP180_MAGIC, BMP180_GET_PRES, char*)
#define IOCTL_BMP180_GET_ALTI _IOR(BMP180_MAGIC, BMP180_GET_ALTI, char*)
#define IOCTL_BMP180_GET_OSS  _IOR(BMP180_MAGIC, BMP180_GET_OSS,  char*) 
#define IOCTL_BMP180_SET_OSS  _IOW(BMP180_MAGIC, BMP180_SET_OSS,  char*) 
#define IOCTL_BMP180_COEF     _IO (BMP180_MAGIC, BMP180_COEF           )
#define IOCTL_BMP180_DBGMSG   _IO (BMP180_MAGIC, BMP180_DBGMSG         )



